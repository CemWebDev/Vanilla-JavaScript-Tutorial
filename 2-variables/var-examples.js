// Declaring variables using 'var' keyword

//! Declaring the first variable using 'var':
var firstName = "John";
console.log(firstName);


// Assigning a new value to the 'age' variable
var age = 22; // Initially, the age is set to 22
console.log(age); // 22
age = 34; // Later, the age is updated to 34
console.log(age); // 34